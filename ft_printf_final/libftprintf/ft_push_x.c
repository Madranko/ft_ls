char	*ft_push_x(char *str, char c)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] == 'x')
		{
			str[i] = c;
			str[1] = 'x';
		}
		if (str[i] == 'X')
		{
			str[i] = c;
			str[1] = 'X';
		}
		i++;
	}
	return (str);
}
